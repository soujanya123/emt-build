function Toggle(obj){
                if(document.getElementById){
                         var submenu = document.getElementById(obj);
                         if(submenu.style.display == "none")
                         {
                                submenu.style.display = "block";
                         }
                         else
                         {
                                submenu.style.display = "none";
                         }
                 }
         }

